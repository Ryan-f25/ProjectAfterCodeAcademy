public class Loops {

    public static void main(String[] args) {

        //this program prints the numbers 1 to 10
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }

        //this program prints even numbers from 1 to 10
        for(int n=1;n<=10;n++){
            if(n % 2 == 0){
                System.out.println(n);
            }
        }

    }
}
